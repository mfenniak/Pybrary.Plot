        private ScatterSeries CreateBmiSeries(double bmi)
        {
            ScatterSeries series = new ScatterSeries();
            IList<double> x = new List<double>();
            IList<double> y = new List<double>();
            for (double height = 0; height < 2.25; height += 0.1)
            {
                double weight = bmi * (height * height);
                x.Add(weight);
                y.Add(height);
            }
            series.Data.Set(x, y);
            return series;
        }

        private void InitializeManyPlot()
        {
            Plot p = new Plot();
            p.UseNumericXAxis();

            ((NumericXAxis)p.XAxis).Title = "x (degrees)";

            p.YAxes["Default"].Title = "sin[x]";
            ScatterSeries series = new ScatterSeries();
            series.YAxisName = "Default";
            List<double> xArr = new List<double>();
            List<double> yArr = new List<double>();
            for (double x = 0; x < 360; x++)
            {
                xArr.Add(x);
                yArr.Add(Math.Sin(x * Math.PI / 180));
            }
            series.Data.Set(xArr, yArr);
            p.Series["sin[x]"] = series;

            p.YAxes.AddLeft("cos[x]");
            p.YAxes["cos[x]"].Title = "cos[x]";
            p.YAxes["cos[x]"].GridlinesEnabled = false;
            series = new ScatterSeries();
            series.Line.Color = Color.Green;
            series.YAxisName = "cos[x]";
            xArr = new List<double>();
            yArr = new List<double>();
            for (double x = 0; x < 360; x++)
            {
                xArr.Add(x);
                yArr.Add(Math.Cos(x * Math.PI / 180));
            }
            series.Data.Set(xArr, yArr);
            p.Series["cos[x]"] = series;

            p.YAxes.AddRight("tan[x]");
            p.YAxes["tan[x]"].Title = "tan[x]";
            p.YAxes["tan[x]"].GridlinesEnabled = false;
            series = new ScatterSeries();
            series.Line.Color = Color.Red;
            series.YAxisName = "tan[x]";
            xArr = new List<double>();
            yArr = new List<double>();
            for (double x = 0; x < 360; x++)
            {
                double y = Math.Tan(x * Math.PI / 180);
                if (Double.IsInfinity(y) || Double.IsNaN(y) || y > 1e5 || y < -1e5)
                    continue;
                xArr.Add(x);
                yArr.Add(y);
            }
            series.Data.Set(xArr, yArr);
            p.Series["tan[x]"] = series;

            manyPlotControl.Plot = p;
        }

